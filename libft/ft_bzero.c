#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*stemp;
	size_t			i;

	stemp = s;
	i = 0;
	while (i < n)
	{
		stemp[i] = '\0';
		i++;
	}
}
