#include "libft.h"

size_t	ft_intcount(int n)
{
	size_t	len;
	long	n2;

	len = 0;
	n2 = n;
	if (n2 < 0)
		n2 = -n2;
	if (n2 == 0)
		return (1);
	while (n2 > 0)
	{
		n2 = n2 / 10;
		len++;
	}
	return (len);
}
